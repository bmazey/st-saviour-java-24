package oop.automobiles;

public class Car {

    private String drive;

    public void setDrive(String drive) {
        this.drive = drive;
    }
    public String getDrive() {
        return this.drive;
    }
    public String wheels;
    public void setWheels(String wheels) {
        this.wheels = wheels;
    }
    public String getWheels() {
        return this.wheels;
    }
}
